public class Switch{
    public static void main (String args[]) {
        int number = 2;
        switch(number) {
            case 1 : System.out.println("Pizza");
            case 2 : System.out.println("Burger");
            case 3 : System.out.println("Mango Shake");              
            default : System.out.println("We wake up");


        }
    }
}